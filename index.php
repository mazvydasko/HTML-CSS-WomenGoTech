            <div class="firstContainer">
                <h2>Kaip tai veikia?</h2>
                
                <p>
                    Šalia gausaus renginių ciklo programa siūlo dvi kryptis: mentorystę bei konsultacijas, į kurias kandidatės bus paskirtos pagal jų asmeninį poreikį. MENTORYSTĖ vykdoma individualiai (angl. one to one sessions), susitinkant bent kartą per mėnesį, dalinantis žiniomis ir patirtimi, padedant ugdytinei rasti atsakymus į kylančius klausimus bei nepasiklysti, siekiant programos pradžioje užsibrėžtų tikslų.
                </p>
                
                <p>
                    KONSULTACIJOS skirtos merginoms, kurios jau yra pasirinkusios TECH kryptį ir dega noru praplėsti savo turimus įgūdžius ir patirtis specifinėmis techninėmis žiniomis, konkrečių įrankių pažinimu ir pritaikomumu. Sesijos su patarėjais vyksta mažose iki 10 žmonių grupėse klausimų-atsakymų formatu.
                </p>
                
                <p>
                    Įsipareigojimo mokestis atrinktoms dalyvėms – 50 EUR už viso sezono veiklas. Jei negali sau tiek leisti, kandidatuodama būtinai paminėk ir rasime išeitį.
                </p>
            </div>
            
