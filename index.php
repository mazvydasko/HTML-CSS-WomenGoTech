                        <h2>Kodėl?</h2>
                        <p>
                            Darbas technologijų sektoriuje nepaprastai įdomus – vis daugiau produktų yra kuriami arba perkeliami į skaitmenines erdves, augimo tempai didėja, o galimybės industrijoje platėja. Nenuostabu, kad dėl to darbo rinka susiduria su vis didėjančiu talentų trūkumu, tačiau mus vis dar stebina faktas, kad moterų skaičius šioje srityje tesudaro vos 20 proc. technologijose dirbančių specialistų. Norėdami pakeisti mąstymą bei stereotipus, jog technologijos yra vyriška sritis, su „Women Go Tech“ komanda ir technologijų lyderiais Lietuvoje pristatome programą, skirtą paskatinti moteris atrasti savo kelią technologijų srityje. Programa įkvepia pokyčiams: nuo išaugusio pasitikėjimo iki naujo darbo, tą gali patvirtinti Alumni sėkmės istorijos.
                        </p>
